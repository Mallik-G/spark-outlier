import scala.collection.mutable.HashMap

class JobConf {

  private val props = HashMap.empty[String,Any]
  
  def set(k:String,v:Any) {
    props += k -> v
  }

  def get(k:String):Option[Any] = props.get(k)
  
  def getProps() = props
  
}